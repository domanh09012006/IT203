package SESSION09;

public class demi {
    private double a;
    private  double b;
    public demi(double a, double b) {
        super();
        this.a = a;
        this.b = b;
    }
    public static double C(double a, double b) {
        return 2*a + 2*b;
    }
    public static double S( double a, double b) {
        return a*b;
    }
}
